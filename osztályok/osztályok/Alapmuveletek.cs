using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace osztályok
{
    class Alapmuveletek
    {
        public int Osszead(int a, int b) 
        {
          return a + b;
        }
        public int Kivon(int a, int b)
        {
            return a - b;
        }
        public int Szoroz(int a, int b)
        {
            return a * b;
        }
        public double Oszt(double a, double b)
        {
            return a / b;
        }
    }
}
